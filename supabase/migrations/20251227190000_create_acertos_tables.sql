-- Create ACERTOS table
CREATE TABLE IF NOT EXISTS public.ACERTOS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    DATA_ACERTO TIMESTAMP WITH TIME ZONE DEFAULT NOW(),
    CLIENTE_ID BIGINT NOT NULL, -- References CLIENTES(CODIGO)
    FUNCIONARIO_ID BIGINT NOT NULL, -- References FUNCIONARIOS(id)
    VALOR_TOTAL DECIMAL(10, 2) DEFAULT 0,
    OBSERVACOES TEXT,
    CREATED_AT TIMESTAMP WITH TIME ZONE DEFAULT NOW()
);

-- Create ITENS_ACERTO table
CREATE TABLE IF NOT EXISTS public.ITENS_ACERTO (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    ACERTO_ID BIGINT REFERENCES public.ACERTOS(ID) ON DELETE CASCADE,
    PRODUTO_ID BIGINT NOT NULL, -- References PRODUTOS(ID)
    SALDO_INICIAL INTEGER DEFAULT 0,
    CONTAGEM INTEGER DEFAULT 0,
    QUANT_VENDIDA INTEGER DEFAULT 0,
    PRECO_UNITARIO DECIMAL(10, 2) DEFAULT 0,
    VALOR_VENDIDO DECIMAL(10, 2) DEFAULT 0,
    SALDO_FINAL INTEGER DEFAULT 0
);

-- Create indexes for performance
CREATE INDEX IF NOT EXISTS idx_acertos_cliente_id ON public.ACERTOS(CLIENTE_ID);
CREATE INDEX IF NOT EXISTS idx_acertos_funcionario_id ON public.ACERTOS(FUNCIONARIO_ID);
CREATE INDEX IF NOT EXISTS idx_itens_acerto_acerto_id ON public.ITENS_ACERTO(ACERTO_ID);

-- Enable RLS (Optional but recommended, allowing public access for now as per project pattern implied)
ALTER TABLE public.ACERTOS ENABLE ROW LEVEL SECURITY;
ALTER TABLE public.ITENS_ACERTO ENABLE ROW LEVEL SECURITY;

